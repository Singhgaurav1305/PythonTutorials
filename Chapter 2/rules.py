# Rules for defining variables in Python

# 1. Variable names must start with a letter (a-z, A-Z) or an underscore (_)
# 2. The rest of the variable name can contain letters, digits (0-9), or underscores (_)
# 3. Variable names are case-sensitive (e.g., myVar and myvar are different)
# 4. Variable names cannot be a reserved keyword in Python (e.g., if, else, while, for, etc.)
# 5. Variable names should not contain spaces; use underscores (_) instead      